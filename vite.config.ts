import path from 'path';
import { defineConfig, loadEnv } from 'vite';
import react from '@vitejs/plugin-react';

export default defineConfig(({ mode }) => {
    const env = loadEnv(mode, '.', '');
    // For GitHub Pages: 
    // - If deploying to root (username.github.io), use base: '/'
    // - If deploying to subdirectory (username.github.io/repo-name), use base: '/repo-name/'
    // Set to '/Portfolio-cool/' to match your GitHub Pages URL
    const base = process.env.VITE_BASE_PATH || '/Portfolio-cool/';
    
    return {
      base: base,
      server: {
        port: 3000,
        host: '0.0.0.0',
      },
      plugins: [react()],
      define: {
        'process.env.API_KEY': JSON.stringify(env.GEMINI_API_KEY),
        'process.env.GEMINI_API_KEY': JSON.stringify(env.GEMINI_API_KEY)
      },
      resolve: {
        alias: {
          '@': path.resolve(__dirname, '.'),
        }
      }
    };
});
